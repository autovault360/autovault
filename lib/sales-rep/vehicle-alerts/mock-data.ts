export function getVehicleAlertTabCounts(
  alerts: ISalesRepVehicleAlert[],
): Record<VehicleAlertTab, number> {
  return {
    all_pending: alerts.filter((a) => a.status !== "resolved").length,
    pending_documents: alerts.filter((a) => a.status === "pending_documents")
      .length,
    under_review: alerts.filter((a) => a.status === "under_review").length,
    needs_changes: alerts.filter((a) => a.status === "needs_changes").length,
  };
}
